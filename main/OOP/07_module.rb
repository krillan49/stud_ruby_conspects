# Чтобы встроить модуль в класс, операторы include, prepend или extend прописываются в теле класса, либо при помощи синтаксиса Class.include SelfInject или Class.extend SelfInject:

# include(*modules) - включает указанные модули (`modules`) в текущий модуль. Это делает методы и константы из включенных модулей доступными в текущем модуле (как если бы они были определены непосредственно в нем). Иерархия наследования обновляется (модули добавляются в список предков). При подключении через него в класс, стандартные методы модуля подключаются как методы экземпляра этого класса, а другой функционал будет доступен в методах экземпляра
class Bbb
  include Some
end
p Bbb.meth     #=> undefined method `meth' for Bbb:Class (NoMethodError)
p Bbb.new.meth #=> "aaa"

# prepend(*modules) - аналогичен `include`, но вставляет модули перед текущим модулем/классом в цепочке поиска методов. Это полезно для переопределения или расширения методов существующего класса/модуля без изменения исходного кода.
module MyModule
  def hello
    puts "Hello from MyModule!"
  end
end
class MyClass
  prepend MyModule # MyModule будет перед MyClass в lookup chain   (переопределяет метод MyClass)
  def hello
    puts "Hello from MyClass!"
  end
end
obj = MyClass.new
obj.hello  #=> "Hello from MyModule!"

# extend(*modules) - добавляет методы из указанных модулей (`modules`) как методы класса текущего модуля или класса, стандартные метоы модуля подключаются как статические методы класса этого класса, а другой функционал будет доступен в методах класса
Aaa.extend Some
class Aaa
end
