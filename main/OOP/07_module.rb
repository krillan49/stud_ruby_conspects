# Минусы подключения методов через include, prepend и extend для декомпозиции классов:
# 1. Когда модулей в класс подключено много, то сложно создавать новые методы в классе или подключать еще модули, тк сложно уследить за нэймингом и могут быть одноименные меоды в разных модулях или новые методы в классе с тем же именем и в итоге последний метод переопределит другие и будет путаница. Особенно плохо, когда такие методы изменяют состояние. Тоесть по сути нарушается инкапсуляция и создается большая запутанность, особенно если в модуле будут использованы методы из класса или других модулей
# 2. Методы такого модуля в отрыве от другого фунционала класса могут быть не полноценны, изза чего их невозможно тестировать тдельно и нужно либо использовать весь класс либо создавать специальные тестовые классы
# 3. Тк это модуль, то у него нет конструктора и соответсвенно задание в нем каких-то значений, определяющих изначальное состояние осложнено и нужно использовать какие-то лэйзи штуки вроде ||=

